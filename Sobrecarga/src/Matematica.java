
public class Matematica {
		
	int somar(int x, int y) { // Criando o método somar do tipo int
		
		//int z = x + y;
		//return z;
		return x + y; // Mostrando que esse método irá retornar o valor de x+y
	}
	
	int somar(int x, int y, int z) {
		
		return x + y + z;
		}
	
	double somar(double x, double y) {
		
		return x + y;
	}
	
}
